export const isURL = (url) => {
  const re = new RegExp(
    '^(?:(?:https?|ftp|file):\\/\\/)?' + // Protocol
    '(?:(?:[^\\s:@\\/\\?#]+:[^\\s:@\\/\\?#]*@)?)' + // User:Pass auth
    '(?:\\[([a-f0-9:.]+)\\]|([^\\/\\?#:]*))' + // IPv6 or domain
    '(?::\\d+)?' + // Port
    '(?:\\/(?:[^\\?#](?:[-a-z0-9_~!$&\'()*+,;=:@.%]|%[a-f0-9]{2})*)*)?' + // Path
    '(?:\\?(?:[^#](?:[-a-z0-9_~!$&\'()*+,;=:@\\/\\?%]|%[a-f0-9]{2})*)*)?' + // Query
    '(?:#(?:[^](?:[-a-z0-9_~!$&\'()*+,;=:@\\/\\?%]|%[a-f0-9]{2})*)*)?$',
    'i' // Case-insensitive flag
  );
  return re.test(url);
};